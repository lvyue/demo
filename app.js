var express = require('express');
var path = require('path');
var logger = require('morgan');
var cookieParser = require('cookie-parser');
var bodyParser = require('body-parser');

var index = require('./routes/index');
var rule = require('./routes/rule');
var filter = require('./routes/filter');
var convert = require('./routes/convert');
var analyze = require('./routes/analyze');
var download = require('./routes/download');
var resource = require('./routes/resource');
var diff = require('./routes/diff');

var app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'ejs');

// uncomment after placing your favicon in /public
// app.use(favicon(path.join(__dirname, 'public', 'favicon.ico')));
app.use(logger('dev'));
// app.use(bodyParser({
// 	uploadDir: './upload/origin'
// }));
app.use(bodyParser.json());
app.use(
    bodyParser.urlencoded({
        extended: true
    })
);
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));
app.use('/upload', express.static(path.join(__dirname, 'upload')));

app.use('/', index);
app.use('/rules', rule);
app.use('/filter', filter);
app.use('/convert', convert);
app.use('/analyze', analyze);
app.use('/downloads', download);
app.use('/resources', resource);
app.use('/diff', diff);
app.use('/api', require('./routes/api/mapping'));

// catch 404 and forward to error handler
app.use(function (req, res, next) {
    var err = new Error('Not Found');
    err.status = 404;
    next(err);
});

// error handler
app.use(function (err, req, res) {
    // set locals, only providing error in development
    res.locals.message = err.message;
    res.locals.error = req.app.get('env') === 'development' ? err : {};

    // render the error page
    res.status(err.status || 500);
    res.render('error');
});

module.exports = app;
